package com.items.api.pojo;

public class BookPrice {
    private String title;
    private int price;

    public BookPrice() {
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
